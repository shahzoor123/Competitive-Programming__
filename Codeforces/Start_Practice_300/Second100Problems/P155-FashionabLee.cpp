#include <bits/stdc++.h>
using namespace std;

void solve() {
    long long n;
    cin >> n;

    if (n % 4 == 0) {
        cout << "YES" << "\n";
        return;
    }
    else {
        cout << "NO" << endl;
        return;
    }
    
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
